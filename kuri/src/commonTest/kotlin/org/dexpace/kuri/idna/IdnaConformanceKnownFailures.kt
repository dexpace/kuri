    fun all(): Set<String> =
        setOf(
            "",
            "\u001flookout.net",
            "www.lookout.net\u2a7480",
            "www\u00a0.lookout.net",
        )
}

/** Tracked baseline of currently-failing conformance inputs (see [IdnaKnownFailureData]). */
internal val IDNA_KNOWN_FAILURES: Set<String> = IdnaKnownFailureData.all()
